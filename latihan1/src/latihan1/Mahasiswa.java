package latihan1;

public class Mahasiswa {
    
    private String nama;
    private String nim;
    private String tugas;

    public void setNama(String nama) {
        this.nama = nama;
    }
    public void setNim(String nim) {
        this.nim = nim;
    }
    public void setTugas(String tugas) {
        this.tugas = tugas;
    }
    public String getNama() {
        return nama;
    }
    public String getNim() {
        return nim;
    }
    public String getTugas() {
        return tugas;
    }       
}
